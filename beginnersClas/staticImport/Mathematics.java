package staticImport;

public class Mathematics {
	
	public static void Addition(int a, int b) {
		int c  = a+b;
		System.out.println("Addition method result is "+c);
	}
	
	public static void Subtraction(int a, int b) {
		int c  = a-b;
		System.out.println("Subtraction method result is "+c);
	}
	
	public static void Multiplication(int a, int b) {
		int c  = a*b;
		System.out.println("Multiplication method result is "+c);
	}
	
	public static void Division(int a, int b) {
		int c  = a/b;
		System.out.println("Division method result is "+c);
	}

}

